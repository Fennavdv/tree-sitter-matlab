        function testDigraphTreeJsonPayloadSize(tc)
            % Build a 1000-node tree digraph and verify the converted
            % Plotly JSON payload stays within a reasonable size limit.
            nNodes = 1000;
            sources = strings(1, nNodes - 1);
            targets = strings(1, nNodes - 1);
            nodeNames = "N" + (1:nNodes);
            for k = 2:nNodes
                sources(k-1) = nodeNames(floor((k-1)/2) + 1);
                targets(k-1) = nodeNames(k);
            end
            d = digraph(sources, targets);

            fig = figure("Visible", "off");
            plot(d, "Layout", "layered");
            axis off;

            p = plotlyfig(fig, "visible", "off");

            jsonPayload = m2json(p.data) + m2json(p.layout);
            payloadBytes = strlength(jsonPayload);

            maxBytes = 40 * 1024; % 40 KB
            tc.verifyLessThan(payloadBytes, maxBytes, ...
                sprintf("JSON payload is %d bytes, exceeds %d byte limit", ...
                    payloadBytes, maxBytes));
        end

        function testCustomLegendOrder(tc)
            % Verify that a custom legend order specified via
            % legend(lines(ix),...) is preserved in the Plotly output
            % using the per-trace legendrank property.
            % Traces are created in order C, A, B but the legend is
            % reordered to A, B, C via legendrank.
            legends = ["C" "A" "B"];
            customOrderedLegends = ["A" "B" "C"];

            fig = figure(Visible="off");
            n = 100;
            rng("default");
            y = cumsum(randn(n,3));
            lines = plot(1:n,y);

            [~,ix] = ismember(customOrderedLegends,legends);
            legend(lines(ix),legends(ix),Location="northwest",Box="off");

            p = plotlyfig(fig,visible="off");

            tc.verifyNumElements(p.data,3);

            % Traces stay in original creation order (C, A, B).
            actualNames = cellfun(@(d) string(d.name),p.data);
            tc.verifyEqual(actualNames,legends, ...
                "Traces must remain in original creation order");

            % legendrank controls the display order in the legend.
            % A=rank 1, B=rank 2, C=rank 3.
            % Trace 1 is "C" -> rank 3, Trace 2 is "A" -> rank 1,
            % Trace 3 is "B" -> rank 2.
            actualRanks = cellfun(@(d) d.legendrank,p.data);
            tc.verifyEqual(actualRanks,[3 1 2], ...
                "legendrank values must order entries as A, B, C");

            % All three traces should be visible in the legend.
            actualShowLegend = cellfun(@(d) d.showlegend,p.data);
            tc.verifyTrue(all(actualShowLegend), ...
                "All traces should have showlegend=true");

            % traceorder must be 'normal' so Plotly honours legendrank.
            tc.verifyEqual(string(p.layout.legend.traceorder),"normal");
        end

